void print_big_string (vector<vector<vector<char>>> big_str, int number_of_letters) {
/*  Prints each row along a combination of letters.
*/
    for (int row = 0 ; row < rows ; row++) {
    // Row-wise iterations
        for (int letter = 0 ; letter < number_of_letters; letter++) {
        // Letter-wise iterations
            for (int col = 0 ; col < cols ; col++) {
                // Column-wise iterations
                cout << big_str[letter][row][col] ;
            }
        }

        cout << endl ; //at the end of the row, go to the next line
    }

}

